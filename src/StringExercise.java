public class StringExercise {
    public static void main(String[] args) {

//        For each of the following output examples, create a string variable named message
//        that contains the desired output and print it out to the console.
//        Do this with only one string variable and one print statement.

//        We don't need no education
//        We don't need no thought control

        String message1 = "We don't need no education\n" + "We don't need no thought control";
        System.out.println(message1);
        System.out.println();


//        Check "this" out!, "s inside of "s!

        String message2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(message2);
        System.out.println();


//        In windows, the main drive is usually C:\

        String message3 = "In windows, the main drive is usually C:\\";
        System.out.println(message3);
        System.out.println();


        String message4 = "I can do backslashes \\, double backslashes \\\\,\n" + "and the amazing triple backslash \\\\\\!";
        System.out.println(message4);
        System.out.println();
    }
}
